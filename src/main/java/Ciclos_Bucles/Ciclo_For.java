public class Ciclo_For {
    /*
    for(inicializacion; condiciín ; aumento o decremento){
    Instrucciones
    }
    * IMPORTANTE: EN EL BUCLE FOR NO ES NECESARIO EL I++; YA QUE EL AUMENTO SE ESTABLECE COMO TERCER PARÁMETRO
    */
    
    public static void main(String[] args) {
        for(int i = 10; i>=1; i--){
            System.out.println(i);
        }
    }
}
